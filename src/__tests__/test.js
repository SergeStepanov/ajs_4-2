import sortLive from '../index';

test('тест функции sortLive', () => {
  const arrSort = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];

  const arrTotal = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];

  expect(sortLive(arrSort)).toEqual(arrTotal);
});
